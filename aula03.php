          <h1> IBGE divulga as estimativas da população dos municípios para 2019 </h1>

          <p> O IBGE divulga hoje as estimativas das populações residentes nos 5.570 municípios brasileiros, com data de referência em 1º de julho de 2019. Estima-se que o Brasil tenha 210,1 milhões de habitantes e uma taxa de crescimento populacional de 0,79% ao ano, apresentando queda do crescimento quando comparado ao período 2017/2018, conforme a Projeção da População 2018. </p>
          <p> O município de São Paulo continua sendo o mais populoso do país, com 12,25 milhões de habitantes, seguido pelo Rio de Janeiro (6,72 milhões de habitantes), Brasília (3,0 milhões) e Salvador (2,9 milhões). Juntos, os 17 municípios brasileiros com população superior a um milhão de pessoas somam 46,1 milhões de habitantes ou 21,9% da população do Brasil.</p>
